export const getCodeLaguage = (shortName: string) => {
  const language = codeLaguages.find(codeLaguage => {
    return codeLaguage.toLowerCase() === shortName.toLowerCase();
  });

  if (language) {
    return language;
  }

  return laguagesWithShortName[shortName];
};
