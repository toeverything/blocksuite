export const getCodeLanguage = (languageName: string) => {
  const language = codeLanguages.find(codeLanguage => {
    return codeLanguage.toLowerCase() === languageName.toLowerCase();
  });

  if (language) {
    return language;
  }

  return languagesWithShortName[languageName];
};
